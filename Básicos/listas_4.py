# Encuentra el número máximo y mínimo en una lista.

lista_numeros = [1, 9, 10, 2, 300, 5, 90, 150, 27, 56, 3, 76, 78, 560, 703, 498, 978, 965]

print("La lista de números es la siguiente", lista_numeros)

print("El número mínimo es: ",min(lista_numeros))
print("El número máximo es: " ,max(lista_numeros))
